<script setup lang="ts">
import { computed, ref, watchEffect } from 'vue'

import useStore from "@src/store/store";

import {
  BellIcon,
  ChatBubbleOvalLeftIcon,
  Cog6ToothIcon,
  MoonIcon,
  PhoneIcon,
  SunIcon,
  UserIcon,
} from "@heroicons/vue/24/solid";
import AccountDropdown from "@src/components/views/HomeView/Navigation/AccountDropdown.vue";
import Logo from "@src/components/views/HomeView/Navigation/Logo.vue";
import NavLink from "@src/components/views/HomeView/Navigation/NavLink.vue";
import useChatStore from "@src/store/chat";
import { SidebarComponentName } from "@src/typeV2";

const store = useStore();
const chatStore = useChatStore()

const showDropdown = ref(false);

const unreadMsgCount = computed(() => {
  let sumNoReaderNum = 0
  for (const item of chatStore.chatList) {
    sumNoReaderNum += item.no_reader_num
  }
  return sumNoReaderNum
})

const notificationNum = computed(() => chatStore.newFriendTipsNum + chatStore.newGroupTipsNum)

watchEffect(() => {
  if (chatStore.activeSidebarComponent === 'notifications') {
    chatStore.newFriendTipsNum = chatStore.newGroupTipsNum = 0
  }
})

// (event) change the active sidebar component when clicking on a NavLink
const handleActiveSidebarComponentChange = (value: SidebarComponentName) => {
  chatStore.activeSidebarComponent = value;
};
</script>

<template>
  <div
    class="xs:w-full md:w-11 md:h-full md:py-7 xs:py-5 px-5 flex xs:flex-row md:flex-col items-center transition-all duration-500"
  >
    <!--logo-->
    <Logo />

    <!--main navigation-->
    <div class="grow">
      <nav aria-label="Main navigation">
        <ul class="xs:flex md:block xs:justify-between xs:items-center">
          <!--message button-->
          <li>
            <NavLink
              :icon="ChatBubbleOvalLeftIcon"
              :notifications="unreadMsgCount"
              title="Conversations"
              @click="() => handleActiveSidebarComponentChange('messages')"
              :active="chatStore.activeSidebarComponent === 'messages'"
            />
          </li>

          <!--contacts list button-->
          <li>
            <NavLink
              :icon="UserIcon"
              title="Contacts"
              @click="() => handleActiveSidebarComponentChange('contacts')"
              :active="chatStore.activeSidebarComponent === 'contacts'"
            />
          </li>

          <!--dropdown button small screen-->
          <li>
            <AccountDropdown
              id="small-profile-menu"
              class="xs:block md:hidden"
              aria-labelledby="small-profile-menu-button"
              :show-dropdown="showDropdown"
              :handle-show-dropdown="() => (showDropdown = true)"
              :handle-close-dropdown="() => (showDropdown = false)"
            />
          </li>

          <!--notifications button-->
          <li class="xs:hidden md:inline">
            <NavLink
              :icon="BellIcon"
              title="Notifications"
              :notifications="notificationNum"
              @click="() => handleActiveSidebarComponentChange('notifications')"
              :active="chatStore.activeSidebarComponent === 'notifications'"
            />
          </li>

          <!--voice call button-->
          <!-- TODO: voice call-->
          <!--<li>-->
          <!--  <NavLink-->
          <!--    :icon="PhoneIcon"-->
          <!--    title="Call log"-->
          <!--    @click="() => handleActiveSidebarComponentChange('phone')"-->
          <!--    :active="chatStore.activeSidebarComponent === 'phone'"-->
          <!--  />-->
          <!--</li>-->

          <!--settings button small screen-->
          <li class="xs:inline md:hidden">
            <NavLink
              :icon="Cog6ToothIcon"
              title="Settings"
              @click="() => handleActiveSidebarComponentChange('settings')"
              :active="chatStore.activeSidebarComponent === 'settings'"
            />
          </li>
        </ul>
      </nav>
    </div>

    <!--secondary navigation-->
    <div>
      <nav aria-label="Extra navigation" class="xs:hidden md:block">
        <ul>
          <!--toggle dark mode button-->
          <li>
            <NavLink
              :icon="store.settings.darkMode ? SunIcon : MoonIcon"
              title="Night mode"
              @click="store.settings.darkMode = !store.settings.darkMode"
            />
          </li>
          <!--settings button-->
          <li>
            <NavLink
              :icon="Cog6ToothIcon"
              title="Settings"
              @click="() => handleActiveSidebarComponentChange('settings')"
              :active="chatStore.activeSidebarComponent === 'settings'"
            />
          </li>
        </ul>
      </nav>

      <!--separator-->
      <hr
        class="xs:hidden md:block mb-6 border-gray-100 dark:border-gray-600"
      />

      <!--user avatar-->
      <AccountDropdown
        id="profile-menu"
        class="xs:hidden md:block"
        aria-labelledby="profile-menu-button"
        :show-dropdown="showDropdown"
        :handle-show-dropdown="() => (showDropdown = true)"
        :handle-close-dropdown="() => (showDropdown = false)"
      />
    </div>
  </div>
</template>
